      <h2>Allgemeine Informationen</h2>
      <p>Der Dom zu Speyer, offiziell als Kaiserdom zu Speyer bekannt, ist eine der bedeutendsten romanischen Kathedralen Europas. Er wurde 1030 von Kaiser Konrad II. in Auftrag gegeben und 1061 vollendet. Der Dom diente als Grablege für acht deutsche Kaiser und Könige, was seine historische Bedeutung als kaiserliche Ruhestätte unterstreicht.</p>
      <p>Architektonisch beeindruckt der Dom durch seine monumentalen Ausmaße, die charakteristischen Rundbögen und die mächtigen Pfeiler. Die kunstvoll gestaltete Krypta und die Königshalle mit ihrem beeindruckenden Tonnengewölbe sind besonders bemerkenswerte Merkmale.</p>
      <p>1981 wurde der Dom zu Speyer in die Liste des UNESCO-Weltkulturerbes aufgenommen, was seine herausragende Bedeutung als architektonisches und historisches Denkmal würdigt. Der Dom ist nicht nur ein Ort religiöser Bedeutung, sondern auch ein Zeugnis der Macht und des Einflusses des Heiligen Römischen Reiches.</p>
